
public class MeiosDeTransportes {
	
	public String modelo;
	public int numLugares;
	public int comprimento;
	public int ano;
	public String cor;
	
	public MeiosDeTransportes(String modelo, int numLugares, int comprimento, int ano, String cor) {
	
		this.modelo = modelo;
		this.numLugares = numLugares;
		this.comprimento = comprimento;
		this.ano = ano;
		this.cor = cor;
	}
	

}
